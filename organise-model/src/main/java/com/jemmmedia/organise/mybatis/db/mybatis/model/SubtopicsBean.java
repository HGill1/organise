public class SubtopicsBean {
	private long subtopicId;
	private String subtopicName;
	private String subtopicColor;
	/**
	 * @return the subtopicId
	 */
	public long getSubtopicId() {
		return subtopicId;
	}
	/**
	 * @param subtopicId the subtopicId to set
	 */
	public void setSubtopicId(long subtopicId) {
		this.subtopicId = subtopicId;
	}
	/**
	 * @return the subtopicName
	 */
	public String getSubtopicName() {
		return subtopicName;
	}
	/**
	 * @param subtopicName the subtopicName to set
	 */
	public void setSubtopicName(String subtopicName) {
		this.subtopicName = subtopicName;
	}
	/**
	 * @return the subtopicColor
	 */
	public String getSubtopicColor() {
		return subtopicColor;
	}
	/**
	 * @param subtopicColor the subtopicColor to set
	 */
	public void setSubtopicColor(String subtopicColor) {
		this.subtopicColor = subtopicColor;
	}
}
